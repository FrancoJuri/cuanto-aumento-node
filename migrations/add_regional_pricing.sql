-- 1. New table: regional_prices
CREATE TABLE regional_prices (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_ean TEXT NOT NULL REFERENCES products(ean),
  supermarket_id BIGINT NOT NULL REFERENCES supermarkets(id),
  postal_code TEXT NOT NULL,
  region_label TEXT,
  sku_id TEXT,
  seller_id TEXT,
  seller_name TEXT,
  price NUMERIC,
  list_price NUMERIC,
  is_available BOOLEAN DEFAULT true,
  last_checked_at TIMESTAMPTZ DEFAULT now(),
  created_at TIMESTAMPTZ DEFAULT now(),
  UNIQUE(product_ean, supermarket_id, postal_code)
);

-- 2. New table: regional_price_history
CREATE TABLE regional_price_history (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  regional_price_id BIGINT NOT NULL REFERENCES regional_prices(id),
  price NUMERIC NOT NULL,
  list_price NUMERIC,
  scraped_at TIMESTAMPTZ DEFAULT now()
);

-- 3. Add columns to supermarket_products (nullable, no constraint changes)
ALTER TABLE supermarket_products ADD COLUMN IF NOT EXISTS sku_id TEXT;
ALTER TABLE supermarket_products ADD COLUMN IF NOT EXISTS seller_id TEXT;
ALTER TABLE supermarket_products ADD COLUMN IF NOT EXISTS seller_name TEXT;

-- 4. Indexes for query performance
