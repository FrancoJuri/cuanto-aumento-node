CREATE INDEX idx_regional_prices_supermarket ON regional_prices(supermarket_id);
CREATE INDEX idx_regional_prices_last_checked ON regional_prices(last_checked_at);
CREATE INDEX idx_regional_price_history_regional_id ON regional_price_history(regional_price_id);
CREATE INDEX idx_regional_price_history_scraped ON regional_price_history(scraped_at);

